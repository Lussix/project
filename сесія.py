import math

# Функція для обчислення мінімальної вартості квитків
def calculate_min_cost(n, m, a, b, c, k):
    # Вартість одноразових квитків
    cost_single = n * m * a
    
    # Вартість індивідуальних безлімітних квитків
    cost_individual = n * b
    
    # Вартість групових безлімітних квитків
    num_groups = math.ceil(n / k)
    cost_group = num_groups * c
    
    # Повертаємо мінімальну вартість
    return min(cost_single, cost_individual, cost_group)

# Основна програма
def main():
    # Введення даних
    n = int(input("Введіть кількість людей (n): "))
    m = int(input("Введіть кількість поїздок на людину (m): "))
    a = float(input("Введіть вартість одноразового квитка (a): "))
    b = float(input("Введіть вартість індивідуального безлімітного квитка (b): "))
    c = float(input("Введіть вартість групового безлімітного квитка (c): "))
    k = int(input("Введіть кількість людей для групового квитка (k): "))
    
    # Виклик функції
    min_cost = calculate_min_cost(n, m, a, b, c, k)
    
    # Виведення результату
    print(f"Мінімальна сумарна вартість квитків: {min_cost} грн")

# Запуск програми
if __name__ == "__main__":
    main()
